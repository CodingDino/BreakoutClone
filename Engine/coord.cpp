// Coord
//		Simple struct-like class containing an x, y float coordinate


// |----------------------------------------------------------------------------|
// |								Includes									|
// |----------------------------------------------------------------------------|
#include "Coord.h"


// |----------------------------------------------------------------------------|
// |						   Default Constructor								|
// |----------------------------------------------------------------------------|
Coord::Coord() :
	x(0), y(0)
{
}

	
// |----------------------------------------------------------------------------|
// |						    Copy Constructor								|
// |----------------------------------------------------------------------------|
Coord::Coord(const Coord& other)
	: x(other.x), y(other.y)
{
}


// |----------------------------------------------------------------------------|
// |						     Deconstructor									|
// |----------------------------------------------------------------------------|
Coord::~Coord()
{
}


// |----------------------------------------------------------------------------|
// |							Other Constructors								|
// |----------------------------------------------------------------------------|
Coord::Coord(const float new_x, const float new_y) :
	x(new_x), y(new_y)
{
}
	

// |----------------------------------------------------------------------------|
// |							  Operator =									|
// |----------------------------------------------------------------------------|
Coord Coord::operator= (const Coord& other)
{
	x = other.x; y = other.y;
	return Coord(other);
}
Coord Coord::operator= (const float uniform_value)
{
	return Coord(uniform_value, uniform_value);
}